      <h3>Sign #1: The "Pink" on the Radar (Ice vs. Snow)</h3>
      <p>
        If you are looking at a weather map and you see blue (snow), that‚Äôs good. But if you see pink or purple (ice/mixed precipitation), that is the jackpot.
      </p>
      <p>
        The biggest misconception about snow days is that they require massive amounts of snow. They don't. In fact, deep snow is often easier to manage than a thin layer of ice.
      </p>

      <h4>Why Ice is the "Snow Day" MVP</h4>
      <p>
        School buses are incredibly heavy vehicles, often weighing over 30,000 pounds. While they handle deep snow relatively well due to their weight and dual rear tires, they are helpless on ice. Ice provides zero coefficient of friction.
      </p>
      <p>
        When a Superintendent looks at the forecast, they are terrified of one specific phrase: Freezing Rain Advisory.
      </p>
      <p>
        If the forecast calls for freezing rain even just a tenth of an inch the probability of a school closure skyrockets. If you check an ice day calculator (which is essentially what our tool becomes during these storms), you will see percentages in the 90s even if the snow total is zero.
      </p>
      <p>
        The "Before Bed" Check: Look at the detailed forecast text. If you see words like "glaze," "ice accumulation," or "freezing rain," you can probably skip the homework. Ice is nearly impossible to clear quickly enough for the morning commute.
      </p>

      <h3>Sign #2: The Timing Window (The 4 AM Rule)</h3>
      <p>
        You might see a forecast for 8 inches of snow and think, "I'm definitely staying home." But wait. When is that snow arriving?
      </p>
      <p>
        The decision to close school is usually made between 4:30 AM and 5:30 AM. The goal is to make the call before the first bus leaves the depot. This means the condition of the roads <em>at that specific time</em> matters more than anything else.
      </p>

      <h4>The Three Scenarios</h4>
      <ul>
        <li><strong>Scenario A: The Early Bird (Snow starts at 2:00 AM).</strong> This is the ideal snow day scenario. By the time 5:00 AM rolls around, there are 3 inches on the ground and it's still coming down. The plows haven't caught up. <strong>Chance of Closure: High.</strong></li>
        <li><strong>Scenario B: The Rush Hour Attack (Snow starts at 6:00 AM).</strong> This is a nightmare for Superintendents. The roads are clear when they make the decision at 5:00 AM, but the snow is about to start. They often try to stay open, hoping to beat the storm, or they call a 2-hour delay. <strong>Chance of Closure: Moderate/Risky.</strong></li>
        <li><strong>Scenario C: The Late Arrival (Snow starts at 10:00 AM).</strong> If the snow starts after school has begun, you are almost certainly going to school. Districts hate doing "early dismissals" because sending young children home to potentially empty houses is a liability. They will often force the school day to finish even if snow is piling up. <strong>Chance of Closure: Near Zero.</strong></li>
      </ul>
      <p>
        The "Before Bed" Check: Look at the <em>hourly</em> forecast, not the daily total. If the heaviest precipitation is scheduled for the pre-dawn hours, your snow day predictor results will be much more favorable.
      </p>

      <br>
