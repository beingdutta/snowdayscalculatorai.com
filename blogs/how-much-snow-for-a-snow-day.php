      </p>

      <h4>The "Snow Belt" (Midwest, Northeast, Canada)</h4>
      <p>
        <strong>Threshold: 6+ Inches (or Extreme Ice)</strong>
        <br>
        In these regions, schools are built for winter. A <em>snow day predictor</em> won't even blink at 3 inches. Buses have chains, and road crews work overnight.
        <br>
        To get a cancellation here, you typically need:
      </p>
