      </p>

      <h4>The "Sun Belt" (South, Southeast, Texas)</h4>
      <p>
        <strong>Threshold: 0.5 - 1 Inch (or any Ice)</strong>
        <br>
        In the South, there is no such thing as a "small" snow storm. Because the ground is warm, snow melts on contact and then refreezes overnight into a sheet of ice. Since these cities often have <em>zero</em> salt trucks, roads become skating rinks.
        <br>
        Here, a <em>snow calculator</em> will often predict a 90% chance of closure for just a "dusting" of snow.
      </p>

