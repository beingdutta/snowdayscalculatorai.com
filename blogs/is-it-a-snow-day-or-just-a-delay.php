        </details>

        <details>
          <summary>Does a delay count as a school day?</summary>
          <p>Yes! This is why Superintendents love them. Even though the day is shorter, it counts toward the state-mandated 180 days of instruction, meaning they don't have to extend the school year into summer.</p>
        </details>

        <details>
          <summary>Can a delay turn into a snow day?</summary>
          <p>Yes. This is called a "Re-evaluation." If road crews report that they cannot clear the streets by the delayed start time (usually 9:30 AM or 10:00 AM), the district will upgrade the delay to a full closure.</p>
        </details>

        <details>
          <summary>How does the snow day calculator predict delays?</summary>
          <p>The calculator looks for "borderline" probabilities (40-60%). If the snow volume is moderate but the timing is early morning, the algorithm recognizes a high probability of a delay rather than a full closure.</p>
        </details>

        <details>
          <summary>Are delays common in college?</summary>
          <p>Yes, colleges actually prefer delays over closures. Since students live on campus or commute short distances, a delay allows grounds crews to clear the sidewalks ("walkability") without cancelling valuable lecture time.</p>
