        <li><strong>Walkability:</strong> Can facilities clear the sidewalks?</li>
        <li><strong>Staffing:</strong> Can the professors and cafeteria staff drive in?</li>
        <li><strong>Power:</strong> Is the campus grid stable?</li>
      </ol>
      <p>
        Commuter colleges behave like high schools, but residential universities usually need 10+ inches or severe ice to cancel classes.
