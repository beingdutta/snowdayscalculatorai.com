        </details>

        <details>
          <summary>Does the snow day calculator work for colleges?</summary>
          <p>Yes, but you need to interpret the results differently. A "Snow Day Calculator College" prediction needs a higher threshold. If the calculator says 80%, a college might still be open. Look for 90%+ probabilities for a university closure.</p>
        </details>

        <details>
          <summary>Can a professor cancel class if the university is open?</summary>
          <p>Absolutely. This is the "Rogue Professor" factor. Even if the administration keeps the campus open, individual professors often cancel classes if they cannot safely drive to campus. Always check your email.</p>
        </details>

        <details>
          <summary>What is the difference between commuter and residential colleges for snow days?</summary>
          <p>Commuter colleges act like high schools; if the roads are bad, they close. Residential colleges act like small cities; since the students are already there, they tend to stay open unless the campus infrastructure itself fails.</p>
        </details>

        <details>
          <summary>Will I have a snow day tomorrow if I have an online class?</summary>
          <p>Usually, no. Unless the university declares a total administrative shutdown or there are widespread power outages, online classes typically proceed as scheduled regardless of the snow.</p>
        </details>

        <details>
          <summary>What if I can't safely get to campus but the college is open?</summary>
